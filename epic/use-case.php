            <h2>Jesse Smith Use Case</h2>

            <p><strong>Overview:</strong> Jesse, after receiving targeted marketing based on his persona will begin to access our site and explore targeted products in the health and wellness section.</p>

            <p><strong>When:</strong>At work, the company restricts navigation to our site. Therefore he will be accessing our site primarily in the evening or on break.</p>

            <p><strong>Utilized Tech:</strong> As a result of the above access time restrictions, the primary utilized tech will take two forms: smart phone and personal computer. His persona age indicates that he is likely to be using a laptop rather than a desktop. Further more he is likely to be using Chrome, Firefox, or Safari as his primary browser. He is a mainstream user so we should expect a OSX or Windows platform.</p>

            <p><strong>Conclusions:</strong> Jesse will need a site that understands his needs and can effectively target him. Based on his persona he needs a site that allows him to efficiently purchase and rate products.</p>

            <h2>Interaction Flow</h2>
                <ol>
                    <li>Jesse navigates to site</li>
                    <li>Site presents Jesse with login</li>
                    <li>Jesse logs in</li>
                    <li>Site directs Jesse to his profile with targeted marketing</li>
                    <li>Jesse clicks on marketing</li>
                    <li>Site directs him to products</li>
                    <li>Jesse likes product a,b,c, etc</li>
                    <li>Site records Jesse's likes</li>
                </ol>



